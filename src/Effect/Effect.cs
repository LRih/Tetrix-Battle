using System;
using SwinGame;
using Color = System.Drawing.Color;

namespace TetrixBattle.src
{
    //#==============================================================
    //# * Effect
    //#==============================================================
    public abstract class Effect
    {
        //#----------------------------------------------------------
        //# * Variables
        //#----------------------------------------------------------
        protected int _tick = 0;
        protected int _maxTick;
        //#----------------------------------------------------------
        //# * Initialize
        //#----------------------------------------------------------
        public Effect(int maxTick)
        {
            _maxTick = Math.Abs(maxTick);
        }
        //#----------------------------------------------------------
        //# * Update
        //#----------------------------------------------------------
        public virtual void Update()
        {
            _tick++;
        }
        //#----------------------------------------------------------
        //# * Update Draw
        //#----------------------------------------------------------
        public abstract void UpdateDraw();
        //#----------------------------------------------------------
        //# * Is Expired
        //#----------------------------------------------------------
        public bool IsExpired()
        {
            return _tick >= _maxTick;
        }
        //#----------------------------------------------------------
        //# * Current X
        //#----------------------------------------------------------
        protected abstract int CurrentX
        {
            get;
        }
        //#----------------------------------------------------------
        //# * Current Y
        //#----------------------------------------------------------
        protected abstract int CurrentY
        {
            get;
        }
    }
}